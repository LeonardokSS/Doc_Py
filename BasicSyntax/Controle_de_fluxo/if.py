palavra = str(input("Digite sua palava:"))

if (palavra == "banana"):
    print("A sua palavra é banana! ")
if (palavra == "pera"):
    print("A sua palavra é pera!  ")
if (palavra == "hamburguer"):
    print("A sua palavra é hamburguer! ")

else:
    print("A sua palavra não é nem banana, nem pera ou hamburguer!")
